namespace XPDev.FlightManagement
{
    /// <summary>
    /// The possible positions of a flap lever.
    /// </summary>
    public enum FlapsLeverPosition
    {
        /// <summary>
        /// The flaps lever is in the retracted position.
        /// </summary>
        FlapsRetracted = 0,
        /// <summary>
        /// The flaps lever is in the flaps one position.
        /// </summary>
        Flaps1,
        /// <summary>
        /// The flaps lever is in the flaps two position.
        /// </summary>
        Flaps2,
        /// <summary>
        /// The flaps lever is in the flaps three position.
        /// </summary>
        Flaps3,
        /// <summary>
        /// The flaps lever is in the flaps full position.
        /// </summary>
        FlapsFull
    }
}