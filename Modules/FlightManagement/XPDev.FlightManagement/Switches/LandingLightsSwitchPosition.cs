namespace XPDev.FlightManagement
{
    /// <summary>
    /// Possible position of a landing lights switch.
    /// </summary>
    public enum LandingLightsSwitchPosition
    {
        /// <summary>
        /// The switch is on the Off position.
        /// </summary>
        Off = 0,
        /// <summary>
        /// The switch is on the Retracted position.
        /// </summary>
        Retracted,
        /// <summary>
        /// The switch is on the On position.
        /// </summary>
        On
    }
}